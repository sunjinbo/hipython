# 加+ 减- 乘* 除/
print(2 + 3)
print(3 - 2)
print(5 * 2)
print(5 / 3)


# 整除//
print(13 // 4)


# 取模（返回除法运算后余数）%
print(13 % 4)


# 左移<< 右移>>
print(2 << 2)
print(11 >> 1)


# 按位与& 按位或| 按位异或^ 按位取反~
print(5 & 3)
print(5 | 3)
print(5 ^ 3)
print(~5)

# 小于< 大于> 小于等于<= 大于等于>= 等于==
print(5 < 3)
print(3 > 5)
print(4 <= 3)
print(1 >= 0)
print(4 == 4)
print(3 != 2)


# not(布尔非) and(布尔与) or(布尔或)
print(not True)
print(True and False)
print(True or False)

